        // –ü–æ–ª—É—á–∞–µ–º –∫–∞—Ä—Ç—ã
        const cardsRes = await getClientCards();
        if (cardsRes.data) dispatch(setCards(cardsRes.data));

        // üÜï –ü–æ–ª—É—á–∞–µ–º –∫—Ä–µ–¥–∏—Ç—ã
        const loansRes = await getMyLoans();
